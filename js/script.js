    const modal = document.getElementById('modal');
    const closeModalBtn = document.getElementById('close-modal');
    const speciesTitle = document.getElementById('species-title');
    const speciesImg = document.getElementById('species-img');
    const speciesInfo = document.getElementById('species-info');

    // Render species cards
    speciesList.forEach(species => {
      const card = document.createElement('div');
      card.className = 'species-card';
      card.innerHTML = `
        <img class="species-image" src="${species.img}" alt="${species.name}">
        <div class="species-info">
          <h3 class="species-name">${species.name}</h3>
          <span class="species-status">Endangered</span>
        </div>
      `;
      card.addEventListener('click', () => openModal(species));
      document.getElementById(species.category).appendChild(card);
    });

    function openModal(species) {
      speciesTitle.textContent = species.name;
      speciesImg.src = species.img;
      speciesImg.alt = species.name;
      
      speciesInfo.innerHTML = species.info
        .map(item => `<li>${item}</li>`)
        .join('');
      
      modal.classList.add('active');
      document.body.style.overflow = 'hidden';
    }

    function closeModal() {
      modal.classList.remove('active');
      document.body.style.overflow = 'auto';
    }

    // Event Listeners
    closeModalBtn.addEventListener('click', closeModal);
    window.addEventListener('click', (e) => e.target === modal && closeModal());
    document.addEventListener('keydown', (e) => e.key === 'Escape' && closeModal());
